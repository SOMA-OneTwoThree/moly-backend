    """페르소나 + 응답 언어 고정(유저 입력 언어와 무관하게 profile.language로 생성)."""
    lang = language or "ko"
    return f"{CAPI_PERSONA}\n\n[언어] 무슨 언어로 물어보든 반드시 '{lang}'로만 답한다.\n\n{_OUTPUT_CHECK}"
